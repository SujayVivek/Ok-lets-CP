void Solve() {
    // Your code for each test case goes here
    int n, x;
    cin >> n >> x;
    vector<int> c(n, 0);
    for (auto &val : c) cin >> val;

    // Initialize dp array with size (x + 1), all values are 0
    vector<int> dp(x + 1, 0);
    dp[0] = 1;  // Base case: There's 1 way to make sum 0

    // Iterate through all sums from 0 to x
    for (int i = 0; i <= x; i++) {
        if (dp[i]) {  // If there's a way to make sum i
            for (int j = 0; j < n; j++) {
                if (i + c[j] <= x) {  // Ensure that adding c[j] doesn't exceed the target sum
                    dp[i + c[j]] = (dp[i + c[j]] + dp[i]) % MOD;
                }
            }
        }
    }

    // Output the number of ways to make the sum x
    cout << dp[x] << endl;
}

