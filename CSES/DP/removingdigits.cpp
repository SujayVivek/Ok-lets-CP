vi calc(int n){
    vi digits;
    while(n != 0){
        digits.push_back(n % 10); 
        n /= 10;
    }
    return digits;
}

int rec(int sum, vi &dp){
    if (sum == 0) return 0;      
    if (sum < 0) return 1e9;      
    
    if (dp[sum] != -1) return dp[sum];  
    
    dp[sum] = 1e9; 
    vi digits = calc(sum);  
    
    
    for (int i = 0; i < digits.size(); i++) {
        if (digits[i] != 0 && sum - digits[i] >= 0) { 
            dp[sum] = min(dp[sum], rec(sum - digits[i], dp) + 1);
        }
    }
    
    return dp[sum]; 
}

void Solve() {
    int n;
    cin >> n;
    
    vi dp(n + 1, -1);  
    int result = rec(n, dp); 
    cout << result << endl;  
}

